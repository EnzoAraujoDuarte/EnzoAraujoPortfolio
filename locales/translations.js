// Translation cache
const translationCache = new Map();

/**
 * Get translation for a key in the specified language
 * @param {string} key - Translation key in dot notation (e.g., 'home.title')
 * @param {string} lang - Language code ('en-US' or 'pt-BR')
 * @param {string} [defaultValue] - Optional fallback if translation is missing
 * @returns {string} Translated text or key if translation not found
 */
export function t(key, lang, defaultValue) {
  const cacheKey = `${lang}:${key}`;
  if (translationCache.has(cacheKey)) {
    return translationCache.get(cacheKey);
  }
  
  if (!translations[lang]) {
    console.warn(`Language "${lang}" not supported, falling back to key`);
    return defaultValue || key;
  }

  const translation = translations[lang][key];
  const result = translation !== undefined ? translation : (defaultValue || key);
  
  if (translationCache.size > 100) {
    translationCache.clear();
  }
  translationCache.set(cacheKey, result);
  
  return result;
}

export default translations; 